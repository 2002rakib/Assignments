import math

# Using math.sqrt() to calculate the square root
num = 25
sqrt_value = math.sqrt(num)
print(f"The square root of {num} is: {sqrt_value}")

# Using math.pow() to calculate the power
base = 2
exponent = 3
power_value = math.pow(base, exponent)
print(f"{base} raised to the power of {exponent} is: {power_value}")

# Using math.factorial() to calculate the factorial of a number
fact_num = 5
factorial_value = math.factorial(fact_num)
print(f"The factorial of {fact_num} is: {factorial_value}")

# Using math.radians() to convert degrees to radians
degrees = 90
radians_value = math.radians(degrees)
print(f"{degrees} degrees is equal to {radians_value} radians")

# Using math.pi to get the value of pi
pi_value = math.pi
print(f"The value of pi is: {pi_value}")
