# Creating a sample list
my_list = [10, 20, 5, 40, 30]

# Using sort() - This will sort the list in place 
my_list.sort()
print("List after sort():", my_list)

# Creating a new list for demonstration of sorted()
another_list = [50, 30, 70, 10, 90]

# Using sorted() - This returns a new sorted list without modifying the original list
sorted_list = sorted(another_list)
print("Original list:", another_list)
print("List after sorted():", sorted_list)


