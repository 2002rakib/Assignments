import math

# Using the sqrt() function to calculate the square root
number = 16
square_root = math.sqrt(number)
print(f"The square root of {number} is: {square_root}")


# Using the ceil() function to get the ceiling of a number
decimal_number = 4.3
ceiling_value = math.ceil(decimal_number)
print(f"The ceiling of {decimal_number} is: {ceiling_value}")



# Using the floor() function to get the floor of a number
floor_value = math.floor(decimal_number)
print(f"The floor of {decimal_number} is: {floor_value}")
