# Creating a sample list
my_list = ["apple", 42, 3.14, "banana", True, "Python"]

# Accessing elements using positive indexing
print("Element at index 0:", my_list[0])  # First element

# Accessing elements using negative indexing
print("Last element:", my_list[-1])       # Last element

# Accessing a range of elements (slicing)
print("First three elements:", my_list[:3])  # First three elements
