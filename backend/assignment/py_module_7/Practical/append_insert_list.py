# Creating a sample list
my_list = [1, 2, 3, 4]

# Adding an element using append() - Adds at the end of the list
my_list.append(5)
print("List after append:", my_list)

# Adding an element using insert() - Adds at a specified index position
my_list.insert(2, 10)  # Inserts 10 at index 2
print("List after insert:", my_list)
