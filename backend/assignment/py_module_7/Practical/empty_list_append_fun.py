# Creating an empty list
empty_list = []

# Elements to insert into the empty list
elements_to_add = [10, 20, 30, "Hello", 3.14]

# Using a for loop and append() to add elements to the empty list
for element in elements_to_add:
    empty_list.append(element)

# Printing the updated list
print("List after inserting elements:", empty_list)
