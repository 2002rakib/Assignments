# Creating a tuple with multiple data types
my_tuple = (10, "Hello", 3.14, True, None, [1, 2, 3], {"key": "value"})

# Printing the tuple
print("Tuple with multiple data types:", my_tuple)

# Iterating through the tuple and printing each element and its type
for item in my_tuple:
    print(f"Element: {item}, Type: {type(item)}")
